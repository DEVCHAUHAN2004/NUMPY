import numpy as np

vector = np.array([1,2,3])
print(vector)
# [1 2 3]

matrices = np.array([
                    [1,2,3],
                    [4,5,6]
                    ])
print(matrices)
# [[1 2 3]
#  [4 5 6]]

tensor = np.array([[[1,2],[3,4]],
                   [[5,6],[7,8]]
                  ])
print(tensor)
# [[[1 2]
#   [3 4]]

#  [[5 6]
#   [7 8]]]



